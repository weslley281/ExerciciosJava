package fundamentos;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        int a = 97;
        int b = 'a';

        System.out.println(a == b);
        System.out.println('\u0061');
        System.out.println(3 >= 4);
        System.out.println(3 <= 4);
        System.out.println(3 == 4);
        System.out.println(3 > 4);
        System.out.println(3 < 4);
        System.out.println(3 != 4);
    }
}
